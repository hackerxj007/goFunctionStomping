func getFuntionBase(processHandle w32.HANDLE, dllName string, functionName string) uintptr {
	var moduleListSize w32.DWORD = 0
	isSuccessEnum := winApi.ProcEnumProcessModules(processHandle, nil, 0, &moduleListSize)
	if isSuccessEnum == false {
		fmt.Println("[!] Fail to get module-list Size !")
		return 0
	}

	moduleList := make([]byte, 360)
	isSuccessEnum2 := winApi.ProcEnumProcessModules(processHandle, (*w32.HMODULE)(unsafe.Pointer(&moduleList[0])), moduleListSize, &moduleListSize)

	if isSuccessEnum2 == false {
		fmt.Println("[!] Fail to get module-list !")
		return 0
	}

	for i := 1; i <= int(moduleListSize)/int(unsafe.Sizeof(uintptr(1))); i = i + 1 {
		var currentModule w32.HMODULE
		var currentModuleName string

		fileName := make([]uint16, winApi.MAX_PATH)
		currentModule = *(*w32.HMODULE)(unsafe.Pointer(&moduleList[0+8*(i-1)]))
		isSuccessGetModuleName := winApi.ProcGetModuleFileNameExW(processHandle, currentModule, (*[winApi.MAX_PATH]uint16)(unsafe.Pointer(&fileName[0])), winApi.MAX_PATH-1)

		currentModuleName = syscall.UTF16ToString(fileName)

		if isSuccessGetModuleName != 0 {
			if strings.Contains(currentModuleName, dllName) {
				procAddr, _ := syscall.GetProcAddress(syscall.Handle(currentModule), functionName)
				return procAddr
			}
		}
	}

	return 0

}

